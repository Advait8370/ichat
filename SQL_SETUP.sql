insert into storage.buckets (id, name, public)
values ('uploads', 'uploads', true)
on conflict (id) do update set public = true;

do $$
begin
  create policy "Allow uploads insert" on storage.objects for insert to authenticated with check (bucket_id = 'uploads');
exception when duplicate_object then null;
end $$;

do $$
begin
  create policy "Allow uploads select" on storage.objects for select to public using (bucket_id = 'uploads');
exception when duplicate_object then null;
end $$;

do $$
begin
  create policy "Allow uploads update" on storage.objects for update to authenticated using (bucket_id = 'uploads') with check (bucket_id = 'uploads');
exception when duplicate_object then null;
end $$;

do $$
begin
  create policy "Allow uploads delete" on storage.objects for delete to authenticated using (bucket_id = 'uploads');
exception when duplicate_object then null;
end $$;
